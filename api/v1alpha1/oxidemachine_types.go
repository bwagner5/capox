const MachineFinalizer = "oxidemachine.infrastructure.cluster.x-k8s.io"

// OxideMachineSpec defines the desired state of OxideMachine.
type OxideMachineSpec struct {
	// ProviderID is a unique identifier for the Oxide instance for use in correlating across
	// kubernetes resources. Must match provider IDs generated by the Oxide cloud controller
	// manager.
	// https://cluster-api.sigs.k8s.io/reference/glossary#providerid
	ProviderID string `json:"providerID,omitempty"`

	// NCpus is the number of vCPUs to allocate for the instance.
	NCpus int64 `json:"nCPUs"`
	// Memory is the amount of memory to allocate for the instance, in bytes.
	Memory resource.Quantity `json:"memory"`
	// DiskSize is the size of the boot disk to allocate for the instance, in bytes.
	DiskSize resource.Quantity `json:"diskSize"`

	// ImageID is the ID of the boot disk image.
	ImageID string `json:"imageID"`

	// AntiAffinityGroups is the list of anti-affinity group names or IDs that the Oxide instance
	// should use. Anti-affinity groups should be created outside the operator, else instance
	// creation will fail.
	// https://docs.oxide.computer/guides/deploying-workloads#_affinity_and_anti_affinity
	// +optional
	// +listType=set
	AntiAffinityGroups []string `json:"antiAffinityGroups,omitempty"`

	// SSHPublicKeys is the list of SSH key names or IDs to attach to the Oxide instance. SSH keys
	// should be created outside the operator, else instance creation will fail.
	// https://docs.oxide.computer/guides/user-settings#manage-ssh-keys
	// +optional
	// +listType=set
	SSHPublicKeys []string `json:"sshPublicKeys,omitempty"`

	// IPPool is the Oxide IP pool to use for the instance's external IP. If not set, omit the
	// external IP for the machine. If set, the IP pool must exist.
	// +optional
	IPPool string `json:"ipPool,omitempty"`

	// DataDisks is the list of data disks to attach to the Oxide instance.
	// +optional
	DataDisks []DataDisk `json:"dataDisks,omitempty"`
}

type DataDisk struct {
	// Size is the size of the disk.
	Size resource.Quantity `json:"size"`

	// BlockSize is the block size of the disk.
	// +optional
	// +kubebuilder:validation:Enum=512;2048;4096
	// +kubebuilder:default=4096
	BlockSize int64 `json:"blockSize"`
}

type OxideMachineInitializationStatus struct {
	Provisioned *bool `json:"provisioned,omitempty"`
}

// OxideMachineStatus defines the observed state of OxideMachine.
type OxideMachineStatus struct {
	Initialization OxideMachineInitializationStatus `json:"initialization,omitempty"`

	// For Kubernetes API conventions, see:
	// https://github.com/kubernetes/community/blob/master/contributors/devel/sig-architecture/api-conventions.md#typical-status-properties

	// conditions represent the current state of the OxideMachine resource.
	// Each condition has a unique type and reflects the status of a specific aspect of the
	// resource.
	//
	// Standard condition types include:
	// - "Available": the resource is fully functional
	// - "Progressing": the resource is being created or updated
	// - "Degraded": the resource failed to reach or maintain its desired state
	//
	// The status of each condition is one of True, False, or Unknown.
	// +listType=map
	// +listMapKey=type
	// +optional
	Conditions []metav1.Condition `json:"conditions,omitempty"`

	// addresses contains the associated addresses for the machine.
	// +optional
	Addresses []clusterv1.MachineAddress `json:"addresses,omitempty"`
}

